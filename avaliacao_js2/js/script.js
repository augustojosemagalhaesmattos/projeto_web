document.addEventListener('DOMContentLoaded', function () {
    const seletorCor = document.getElementById('seletorCor');
    const btnFundo = document.getElementById('btnFundo');
    const btnTexto = document.getElementById('btnTexto');
    const btnCaixa = document.getElementById('btnCaixa');

    const texto = document.getElementById('texto');
    const caixa = document.getElementById('caixa');

    btnFundo.addEventListener('click', function () {
        document.body.style.backgroundColor = seletorCor.value;
    });

    btnTexto.addEventListener('click', function () {
        texto.style.color = seletorCor.value;
    });

    btnCaixa.addEventListener('click', function () {
        caixa.style.backgroundColor = seletorCor.value;
    });
});
